                        <h3 class="border-bottom border-primary border-4 d-inline-block pb-1">Mengisi Buku Tamu Online (mandiri)</h3>
                        <p><b><span style="color:#697A8D;">SIMa</span><span class="text-primary">Ta</span></b> adalah sistem manajemen tamu yang dapat digunakan untuk mengelola data tamu dan buku tamu secara online. Untuk mengisi buku tamu online di <b><span style="color:#697A8D;">SIMa</span><span class="text-primary">Ta</span></b>, ikuti langkah-langkah berikut:</p>

                        <ol>
                            <li>Buka halaman <b><span style="color:#697A8D;">SIMa</span><span class="text-primary">Ta</span></b> di browser web Anda.</li>
                            <li>Klik tombol "Buku Tamu" di bagian atas halaman.</li>
                            <li>Isi formulir yang tersedia dengan informasi yang diminta, seperti nama tamu, alamat email, nomor telepon, alamat, dan tujuan kunjungan.</li>
                            <li>Pastikan semua informasi yang dimasukkan sudah benar dan lengkap.</li>
                            <li>Klik tombol "Kirim" untuk menyimpan data tamu ke dalam sistem <b><span style="color:#697A8D;">SIMa</span><span class="text-primary">Ta</span></b>.</li>
                            <li>Jika berhasil, halaman akan menampilkan pesan konfirmasi bahwa data tamu berhasil disimpan.</li>
                        </ol>

                        <p>Selain itu, <b><span style="color:#697A8D;">SIMa</span><span class="text-primary">Ta</span></b> juga menyediakan fitur untuk memonitor data tamu yang sudah masuk dan menampilkan laporan statistik kunjungan tamu. Fitur ini dapat membantu pengguna <b><span style="color:#697A8D;">SIMa</span><span class="text-primary">Ta</span></b> dalam mengelola data tamu dengan lebih efektif.</p>
